void main() {
  
  //unary operators
  //addition
   var a = 10;
  a = a+10;
  print(a);
  //subtraction
  var b = 20;
  b = b - 10;
  print(b);
  //multiplication
  var c = 30;
  c = c* 30;
  print(c);
  //division
  int d  = 40;
  d = d~/40;
  print(d);
  //increment
  var e = 10;
  print(e++);
  print(e);
  //decrement
  var f = 10;
  print(f--);
  print(f);
    
}
